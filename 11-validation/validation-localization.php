৩. বাংলা Validation ব্যবহার করা
config/app.php ফাইলে locale পরিবর্তন করতে হবে:

'locale' => 'bn',
এরপর Laravel স্বয়ংক্রিয়ভাবে সব ভ্যালিডেশন মেসেজ বাংলায় দেখাবে।

৪. Controller উদাহরণ
<?php 
$request->validate([
        'name' => 'required|min:3|max:50',
        'email' => 'required|email|unique:users,email',
    ]);

    // সব ঠিক থাকলে
return back()->with('success', 'ফর্ম সফলভাবে সাবমিট হয়েছে!');
?>

এখন যদি ভ্যালিডেশন ফেইল হয়, এরর মেসেজ বাংলায় দেখাবে।

Laravel 12-এ বাংলা Validation করার জন্য
1️⃣ locale পরিবর্তন
config/app.php ফাইলে এই লাইনটি খুঁজে বের করো:

'locale' => env('APP_LOCALE', 'en'),
এটাকে সরাসরি 'bn' করতে পারো, অথবা .env ফাইল থেকে নিয়ন্ত্রণ করতে পারো।

উদাহরণ .env ফাইল:
APP_LOCALE=bn

