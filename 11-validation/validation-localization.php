১. Validation এরর মেসেজ বাংলায় কাস্টমাইজ করা
Laravel ডিফল্টভাবে এরর মেসেজ ইংরেজিতে দেয়।

তুমি চাইলে এগুলো বাংলায় পরিবর্তন করতে পারো।

এর জন্য resources/lang ফোল্ডারে নতুন ভাষা ফোল্ডার তৈরি করতে হয়।

resources/lang/
    ├── en/
    │    └── validation.php
    └── bn/
         └── validation.php

এখানে bn/validation.php হবে বাংলা মেসেজের ফাইল।

